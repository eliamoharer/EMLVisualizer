interface GenerateOptions {
  depth: number
  families: Family[]
}

export function generateEmlGraph(options: GenerateOptions): EmlGraph {
  const enabledFamilies = new Set<Family>(['primitive', ...options.families])
  const visible = DERIVATION_CHAIN.filter(
    (node) => node.depth <= options.depth && enabledFamilies.has(node.family),
  ).map((node) => ({
    ...node,
    dependencies:
      node.id === 'one' || node.id === 'x_anchor' || !node.formula
        ? []
        : inferExpressionDependencies(node.formula),
  }))
  const visibleIds = new Set(visible.map((node) => node.id))

  const edges: GraphEdge[] = []
  for (const node of visible) {
    for (const dep of node.dependencies) {
      if (visibleIds.has(dep)) {
        edges.push({ id: `e-${dep}-${node.id}`, from: dep, to: node.id })
      }
    }
  }

  return {
    nodes: buildRadialLayout(visible, edges),
    edges,
    rootId: 'one',
  }
}
