function withAliases(extra: string[] = []): string[] {
  const compact = extra.map((value) => value.replaceAll(' ', ''))
  return Array.from(new Set([...extra, ...compact]))
}

function autoTree(
  formula: ClassicalExpr,
  options: { witnessForOne?: boolean; witnessForIdentity?: boolean } = {},
) {
  return compileExpressionToEml(formula, {
    preferWitnessForOne: options.witnessForOne,
    preferWitnessForIdentity: options.witnessForIdentity,
  })
}

